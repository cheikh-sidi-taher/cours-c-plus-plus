#include <iostream>
#include <cmath>
using namespace std;

int main()
{
    cout << "Ceil(2.4) = " << ceil(2.4) << endl; // 3
    cout << "Floor(2.4) = " << floor(2.4) << endl; // 2
    cout << "Ceil(-2.6) = " << ceil(-2.6) << endl; //
    cout << "Floor(-2.6) = " << floor(-2.6) << endl; //
    cout << "Round(2.4) = " << round(2.4) << endl; // 2
    cout << "Round(2.6) = " << round(2.5) << endl; // 3
    cout << "pow(2,3) = " << pow(2,3) << endl; // 8
    cout << "sqrt(4) = " << sqrt(4) << endl; // 2

}