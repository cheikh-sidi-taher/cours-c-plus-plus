#include <iostream>
using namespace std;
int main()
{
   int A = 10, B = 20;
   cout << "B = " << B << endl;
   cout << "A + B = " << A + B << endl;
   cout << "A - B = " << A - B << endl;
   cout << "A * B = " << A * B << endl;
   cout << "A / B = " << A / B << endl;
   cout << "A % B = " << A % B << endl;
  
}